RPDB_Directory* RPDB_Directory_new( char* directory_path )	{
	
	RPDB_Directory*	new_directory	=	calloc( 1, sizeof( RPDB_Directory ) );
	
	new_directory->path	=	directory_path;
	
	return new_directory;
}